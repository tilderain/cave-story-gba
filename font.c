void generate_widths() {
    printf("const uint8_t thinfont_widths[96] = {\n    ");
    
    for (int i = 0; i < 96; i++) {
        int max_col = -1;
        int ascii = i + 32; // Offset by 32 to get actual ASCII char
        
        // Find the rightmost bit set in any of the 12 rows
        for (int row = 0; row < 12; row++) {
            uint8_t bits = thinfontTiles[i * 12 + row];
            for (int col = 0; col < 8; col++) {
                if (bits & (0x80 >> col)) {
                    if (col > max_col) max_col = col;
                }
            }
        }

        int width;
        if (max_col == -1) {
            width = 4; // Space (ASCII 32) default
        } else {
            // Standard: Rightmost pixel index + 1 (width) + 1 (space)
            width = (max_col + 1) + 1;
        }

        // Apply Manual Reductions (-1 pixel)
        // 'M' = 77, 'i' = 105, 'l' = 108
        if (ascii == 'M' || ascii == 'i' || ascii == 'l') {
            width -= 1;
        }

        printf("%d, ", width);
        if ((i + 1) % 16 == 0) printf("\n    ");
    }
    printf("\n};\n");
}

int main() {
    generate_widths();
    return 0;
}
