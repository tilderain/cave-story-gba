static inline volatile uint16_t* bg3_map_entry(int tx, int ty) {
    // BG3 tilemap: 32 entries wide
    return (volatile uint16_t*)BG3_MAP_BASE + ty * 32 + tx;
}

// ---------------------------------------------------------------------------
// canvas_init
// Zero the canvas tiles in VRAM and set up the BG3 tilemap canvas region
// ---------------------------------------------------------------------------
void canvas_init(void) {
    // Default to the bottom window
    int map_row = 14; 
    int map_col = 2;  

    for (int i = 0; i < 1024; i++) {
        BG3_MAP_BASE[i] = 0;
    }

    for (int row = 0; row < CANVAS_TILES_H; row++) {
        for (int col = 0; col < CANVAS_TILES_W; col++) {
            int tile_idx = CANVAS_TILE_BASE + row * CANVAS_TILES_W + col;
            BG3_MAP_BASE[(map_row + row) * 32 + (map_col + col)] = (uint16_t)(tile_idx | (2 << 12));
        }
    }

    canvas_clear();
    BG3HOFS = 0;
    BG3VOFS = 0;
}
// ---------------------------------------------------------------------------
// canvas_clear - zero all canvas tile pixel data (makes canvas transparent)
// ---------------------------------------------------------------------------
void canvas_clear(void) {
    static const uint32_t zero = 0;
    void* dest = (void*)(VRAM_TILE_BASE + (CANVAS_TILE_BASE * 32));
    uint32_t word_count;

    if (s_canvas_is_fullscreen) {
        // Clear all 600 tiles (Be careful: this WILL hit map data if maps are in CB3)
        word_count = (CANVAS_TILES_W_FULL * CANVAS_TILES_H_FULL * 32) / 4;
    } else {
        // Clear only the 156 tiles used by the Window (approx 5KB)
        // This is safe and won't touch CB3/Tilemaps
        word_count = (CANVAS_TILES_W * CANVAS_TILES_H * 32) / 4;
    }

    CpuFastSet(&zero, dest, word_count | (1 << 24));
}

// ---------------------------------------------------------------------------
// canvas_put_glyph - blit one ASCII character into the canvas at pixel (px,py)
// Returns the pixel advance width of the glyph
// ---------------------------------------------------------------------------
int canvas_put_glyph(int px, int py, uint8_t ascii, uint8_t color) {
    if (ascii < 0x20 || ascii > 0x7F) return 4;

    uint8_t glyph_idx = ascii - 0x20;
    
    // Each glyph is exactly 8 bytes long now (1 byte per row)
    const uint8_t *glyph = &thinfontTiles[glyph_idx * 8];
    int advance = thinfont_widths[glyph_idx];

    for (int row = 0; row < 8; row++) {
        uint8_t bits = glyph[row];
        if (!bits) continue;
        
        for (int col = 0; col < 8; col++) {
            // Check bits from left to right (MSB to LSB)
            if (!(bits & (0x80 >> col))) continue;

            int screen_x = px + col;
            int screen_y = py + row;

            // Bounds check against canvas
            if (screen_x < 0 || screen_x >= CANVAS_W) continue;
            if (screen_y < 0 || screen_y >= CANVAS_H) continue;

            // Which canvas tile?
            int tile_col = screen_x >> 3;
            int tile_row = screen_y >> 3;
            int tile_idx = CANVAS_TILE_BASE + tile_row * CANVAS_TILES_W + tile_col;

            // Which pixel within that tile?
            int lx = screen_x & 7;
            int ly = screen_y & 7;

            write_tile_pixel(tile_idx, lx, ly, color);
        }
    }

    return advance;
}

// ---------------------------------------------------------------------------
// canvas_puts - draw a string into the canvas, returns X after last char
// ---------------------------------------------------------------------------
int canvas_puts(int px, int py, const char *str, uint8_t color) {
    int cx = px;
    while (*str) {
        uint8_t c = (uint8_t)*str++;
        if (c == '\n') {
            cx = px;
            py += 8;  // next line (adjust if your line spacing differs)
        } else {
            cx += canvas_put_glyph(cx, py, c, color);
        }
    }
    return cx;
}

// ---------------------------------------------------------------------------
// canvas_set_scroll
// canvas_x = 0 means left edge of canvas visible at left of screen
// We need BG3HOFS to show tilemap pixel CANVAS_MAP_PIXEL_X + canvas_x
// BG3HOFS = offset of the tilemap pixel shown at screen left edge
// ---------------------------------------------------------------------------
void canvas_set_scroll(int canvas_x) {
    BG3HOFS = (uint16_t)(CANVAS_MAP_PIXEL_X + canvas_x);
}

// ---------------------------------------------------------------------------
// canvas_reset_scroll - show the frame, hide the canvas
// ---------------------------------------------------------------------------
void canvas_reset_scroll(void) {
    BG3HOFS = 0;
}

// ---------------------------------------------------------------------------
// canvas_setup_tilemap
// Call this from window_open() AFTER your existing frame tile draw code.
// Re-points the canvas region of the tilemap and clears canvas pixels.
// on_top: matches your windowOnTop flag
// ---------------------------------------------------------------------------

void canvas_setup_tilemap(uint8_t on_top) {
    s_canvas_is_fullscreen = 0; // We are in Window mode
    // TEXT_Y1_TOP is row 1, TEXT_Y1 is row 14
    int map_row = on_top ? 1 : 14;  
    
    // TEXT_X1 is column 2
    int map_col = 2; 

    // Clear the entire 32x32 BG3 tilemap to prevent ghost text from leftover pointers
    for (int i = 0; i < 1024; i++) {
        BG3_MAP_BASE[i] = 0;
    }

    // Map our canvas tiles into the exact tilemap location of the window
    for (int row = 0; row < CANVAS_TILES_H; row++) {
        for (int col = 0; col < CANVAS_TILES_W; col++) {
            int tile_idx = CANVAS_TILE_BASE + row * CANVAS_TILES_W + col;
            
            // Apply palette bank 2 (bits 12-15)
            BG3_MAP_BASE[(map_row + row) * 32 + (map_col + col)] = (uint16_t)(tile_idx | (2 << 12));
        }
    }

    canvas_clear();
    
    // Ensure BG3 hardware scroll offsets are forced to 0 so the tilemap isn't shifted
   // BG3HOFS = 0;
   // BG3VOFS = 0;
}

void canvas_scroll_up(void) {
    // Shift the canvas VRAM up by 16 pixels (2 tile rows)
    int shift_words = (CANVAS_TILES_W * 32 * 2) / 4; 
    int total_words = (CANVAS_TILES_W * CANVAS_TILES_H * 32) / 4;
    
    volatile uint32_t *vram = (volatile uint32_t *)(VRAM_TILE_BASE + CANVAS_TILE_BASE * 32);
    
    // Move the bottom lines of text up
    for (int i = 0; i < total_words - shift_words; i++) {
        vram[i] = vram[i + shift_words];
    }
    
    // Erase the bottom line so new text can be written
    for (int i = total_words - shift_words; i < total_words; i++) {
        vram[i] = 0;
    }
}


void canvas_init_fullscreen(void) {
    s_canvas_is_fullscreen = 1; // We are in Fullscreen mode
    // 1. Wipe the BG3 tilemap memory completely
    volatile uint16_t* map = (volatile uint16_t*)MAP_BASE_ADR(BASE_TEXT); // Using your BG3 macro
    for (int i = 0; i < 1024; i++) {
        map[i] = 0;
    }

    // 2. Map the canvas tiles starting from 0,0 to cover the whole screen
    for (int row = 0; row < CANVAS_TILES_H_FULL; row++) {
        for (int col = 0; col < CANVAS_TILES_W_FULL; col++) {
            int tile_idx = CANVAS_TILE_BASE + (row * CANVAS_TILES_W_FULL) + col;
            
            // Map the tile using palette 2 (or whichever your text uses)
            map[(row * 32) + col] = (uint16_t)(tile_idx | CHAR_PALETTE(2)); 
        }
    }

    // 3. Clear the pixel data inside those tiles
    canvas_clear();
    
    // 4. Reset hardware scroll
    BG_OFFSET[3].x = 0;
    BG_OFFSET[3].y = 0;
}