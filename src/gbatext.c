// Get pointer to a tile's 4bpp data in VRAM (32 bytes per tile)
static inline uint8_t* tile_ptr(int tile_idx) {
    return VRAM_TILE_BASE + tile_idx * 32;
}

// Write a 4bpp pixel into a tile in VRAM
// tile_idx: absolute tile index in VRAM
// lx, ly: local pixel within tile (0..7)
// color: 4bpp palette index (0=transparent)


// ---------------------------------------------------------------------------
// Tilemap helper
// ---------------------------------------------------------------------------

static inline volatile uint16_t* bg3_map_entry(int tx, int ty) {
    // BG3 tilemap: 32 entries wide
    return (volatile uint16_t*)BG3_MAP_BASE + ty * 32 + tx;
}

// ---------------------------------------------------------------------------
// canvas_init
// Zero the canvas tiles in VRAM and set up the BG3 tilemap canvas region
// ---------------------------------------------------------------------------
void canvas_init(void) {
    // Default to the bottom window
    int map_row = 13;
    int map_col = 2;

    for (int i = 0; i < 1024; i++) {
        BG3_MAP_BASE[i] = 0;
    }

    for (int row = 0; row < CANVAS_TILES_H; row++) {
        for (int col = 0; col < CANVAS_TILES_W; col++) {
            int tile_idx = CANVAS_TILE_BASE + row * CANVAS_TILES_W + col;
            BG3_MAP_BASE[(map_row + row) * 32 + (map_col + col)] = (uint16_t)(tile_idx | (2 << 12));
        }
    }

    canvas_clear();
    BG3HOFS = 0;
    BG3VOFS = 0;
}

void canvas_fix_tilemap(uint8_t on_top) {
    int map_col = 2;
    int map_row = on_top ? 1 : (cfg_compact_textbox ? 14 : 13);
    int rows_to_map = cfg_compact_textbox ? 5 : 6;

    // Re-point the tilemap entries to the canvas VRAM tiles
    // We do NOT call canvas_clear() here so existing text stays
    // Map visible rows; remaining rows are a hidden scroll gutter
    for (int row = 0; row < rows_to_map; row++) {
        for (int col = 0; col < CANVAS_TILES_W; col++) {
            int tile_idx = CANVAS_TILE_BASE + (row * CANVAS_TILES_W) + col;
            BG3_MAP_BASE[(map_row + row) * 32 + (map_col + col)] = (uint16_t)(tile_idx | (2 << 12));
        }
    }
}
// ---------------------------------------------------------------------------
// canvas_clear - zero all canvas tile pixel data (makes canvas transparent)
// ---------------------------------------------------------------------------
void canvas_clear(void) {
    static const uint32_t blue_fill = 0x22222222;        // Palette index 2
    static const uint32_t transparent_fill = 0x00000000; // Palette index 0
    
    // Choose fill color based on mode
    // Windowed (s_canvas_is_fullscreen == 0) -> Blue
    // Fullscreen (s_canvas_is_fullscreen == 1) -> Transparent
    const uint32_t *fill_ptr = s_canvas_is_fullscreen ? &transparent_fill : &blue_fill;
    
    void* dest = (void*)(VRAM_TILE_BASE + (CANVAS_TILE_BASE * 32));
    uint32_t word_count;

    if (s_canvas_is_fullscreen) {
        word_count = (CANVAS_TILES_W_FULL * CANVAS_TILES_H_FULL * 32) / 4;
    } else {
        word_count = (CANVAS_TILES_W * CANVAS_TILES_H * 32) / 4;
    }

    // Fill the memory with the chosen color
    CpuFastSet(fill_ptr, dest, word_count | (1 << 24));
}

// ---------------------------------------------------------------------------
// canvas_put_glyph - blit one ASCII character into the canvas at pixel (px,py)
// Returns the pixel advance width of the glyph
// ---------------------------------------------------------------------------
int canvas_put_glyph(int px, int py, uint8_t ascii, uint8_t color) {
    if (ascii < 32 || ascii > 127) return 4;

    uint8_t glyph_idx = ascii - 32;
    const uint8_t *glyph = &thinfontTiles[glyph_idx * 12];
    int advance = thinfont_widths[glyph_idx];
    int stride = s_canvas_is_fullscreen ? CANVAS_TILES_W_FULL : CANVAS_TILES_W;

    for (int row = 0; row < 12; row++) {
        uint8_t bits = glyph[row];
        if (!bits) continue;
        
        for (int col = 0; col < 8; col++) {
            if (!(bits & (0x80 >> col))) continue;

            int screen_x = px + col;
            int screen_y = py + row;

            if (screen_x < 0 || screen_x >= 240) continue;
            if (screen_y < 0 || screen_y >= 160) continue;

            int tile_col = screen_x >> 3;
            int tile_row = screen_y >> 3;
            int tile_idx = CANVAS_TILE_BASE + tile_row * stride + tile_col; // <-- use stride

            write_tile_pixel(tile_idx, screen_x & 7, screen_y & 7, color);
        }
    }

    return advance;
}

void canvas_clear_region(int px, int py, int pixel_w, int pixel_h) {
    int stride = s_canvas_is_fullscreen ? CANVAS_TILES_W_FULL : CANVAS_TILES_W;
    for (int row = 0; row < pixel_h; row++) {
        for (int col = 0; col < pixel_w; col++) {
            int screen_x = px + col;
            int screen_y = py + row;
            if (screen_x < 0 || screen_x >= 240) continue;
            if (screen_y < 0 || screen_y >= 160) continue;
            int tile_col = screen_x >> 3;
            int tile_row = screen_y >> 3;
            int tile_idx = CANVAS_TILE_BASE + tile_row * stride + tile_col;
            write_tile_pixel(tile_idx, screen_x & 7, screen_y & 7, 0);
        }
    }
}


// ---------------------------------------------------------------------------
// canvas_puts - draw a string into the canvas, returns X after last char
// ---------------------------------------------------------------------------
int canvas_puts(int px, int py, const char *str, uint8_t color) {
    int cx = px;
    while (*str) {
        uint8_t c = (uint8_t)*str++;
        if (c == '\n') {
            cx = px;
            py += 8;  // next line (adjust if your line spacing differs)
        } else {
            cx += canvas_put_glyph(cx, py, c, color);
        }
    }
    return cx;
}

// ---------------------------------------------------------------------------
// canvas_set_scroll
// canvas_x = 0 means left edge of canvas visible at left of screen
// We need BG3HOFS to show tilemap pixel CANVAS_MAP_PIXEL_X + canvas_x
// BG3HOFS = offset of the tilemap pixel shown at screen left edge
// ---------------------------------------------------------------------------
void canvas_set_scroll(int canvas_x) {
    BG3HOFS = (uint16_t)(CANVAS_MAP_PIXEL_X + canvas_x);
}

// ---------------------------------------------------------------------------
// canvas_reset_scroll - show the frame, hide the canvas
// ---------------------------------------------------------------------------
void canvas_reset_scroll(void) {
    BG3HOFS = 0;
}

